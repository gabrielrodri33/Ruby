#Exercício para pegar 2 números e realizar todas as operações matemáticas

puts "-=-=-= Operações matemáticas -=-=-="

print "Digite o primeiro número: "
n1 = gets.chomp.to_f

print "Digite o segundo número: "
n2 = gets.chomp.to_f

puts "#{n1} + #{n2} = #{n1 + n2}"

puts "#{n1} - #{n2} = #{n1 - n2}"

puts "#{n1} * #{n2} = #{n1 * n2}"

puts "#{n1} / #{n2} = #{n1 / n2}"

puts "#{n1} % #{n2} = #{n1 % n2}"

puts "#{n1} ** #{n2} = #{n1 ** n2}"